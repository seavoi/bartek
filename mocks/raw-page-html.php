				<h2>Hoof Care</h2>
				<p>The importance of healthy hooves and their impact on the overall wellbeing of the horse has been the subject of numerous scientific research studies that are widely and well understood among professionals in the equine industry.</p>
				<p>The hoof is not a rigid structure, it is a highly flexible and adaptable structure. In barefoot movement the hoof undergoes a 3-dimensional self-reversing deformation. This is the “hoof mechanism” which functions best with a healthy hoof and in turn improves the hoof and horse health by:</p>
				<ol>
					<li>stimulating blood circulation through the hoof and limb</li>
					<li>ensuring good hoof horn quality and growth,</li>
					<li>increasing shock absorption at impact</li>
					<li>maintaining natural biomechanics of the lower limb.</li>
				</ol>
				<p>The continuous interaction between these four factors and the hoof machanism in turn creates a sustainable system for long term health of the horse.</p>
				<p>Once you understand the importance of this system and how it works, it will become clear that, under certain circumstances, when protect of the hoof is needed, it needs to allow the natural hoof mechanism to function properly.</p>

